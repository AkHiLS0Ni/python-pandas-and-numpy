"""## Understanding Tuples

Tuples are an ordered, immutable collection of items. This means that once a tuple is created, you cannot change its contents (add, remove, or modify elements). They are defined using parentheses `()` and elements are separated by commas. While lists are suitable for collections of items that might change, tuples are used for fixed collections.

**Key characteristics of tuples:**
*   **Ordered**: The order of items in a tuple is preserved.
*   **Immutable**: Once created, elements cannot be changed, added, or removed.
*   **Indexed**: Elements can be accessed by their integer index.
*   **Allow duplicates**: Tuples can contain duplicate values.
*   **Heterogeneous**: Tuples can store items of different data types.
"""

# Creating a tuple
my_tuple = (1, 'hello', 3.14, True)
print(f"My tuple: {my_tuple}")
print(f"Type of my_tuple: {type(my_tuple)}")

# Accessing elements (just like lists, using indexing)
print(f"First element: {my_tuple[0]}")
print(f"Last element: {my_tuple[-1]}")

# Slicing tuples (also similar to lists)
print(f"Slice from index 1 to 3 (exclusive): {my_tuple[1:3]}")

"""### Tuple Methods

Tuples have fewer methods than lists because of their immutability. The most common methods are `count()` and `index()`.
"""

my_tuple_with_duplicates = (1, 2, 2, 3, 4, 2)

# count(): Returns the number of times a specified value occurs in a tuple
print(f"Number of '2's in my_tuple_with_duplicates: {my_tuple_with_duplicates.count(2)}")

# index(): Searches the tuple for a specified value and returns the position of where it was found
# If the value is not found, it raises a ValueError
print(f"Index of '3' in my_tuple_with_duplicates: {my_tuple_with_duplicates.index(3)}")

# You can also use 'in' operator to check for membership
print(f"Is '4' in my_tuple_with_duplicates? {'4' in my_tuple_with_duplicates}")

