"""### Understanding `extend()` vs `insert()`

When you used `courses.insert(0, courses_2)` previously, it inserted the entire `courses_2` list `['Tamil', 'English']` as a single element at the beginning of `courses`. This resulted in a nested list where `['Tamil', 'English']` became the first item.

The `extend()` method, on the other hand, takes an iterable (like another list) and adds *each element* of that iterable individually to the end of the current list. If you want to merge two lists without nesting them, `extend()` is the appropriate method.

To demonstrate `extend()`, let's first reset the `courses` list to a simpler state to see the effect clearly.
"""

courses = ['Maths','Sci','English','Hindi','Physics']
courses_2 = ['Tamil','Art']

print(f"Original courses list: {courses}")
print(f"List to extend with: {courses_2}")

courses.extend(courses_2)
print(f"Courses list after extend(): {courses}")

"""As you can see, `extend()` added 'Tamil' and 'Art' as individual elements to the end of the `courses` list, rather than adding `['Tamil','Art']` as a single, nested element."""

