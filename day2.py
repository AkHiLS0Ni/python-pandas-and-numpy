"""## Understanding Sets

Sets are unordered collections of **unique** items. This means that a set cannot have duplicate elements. They are mutable, which implies that you can add or remove items from a set after it's created. Sets are commonly used for tasks involving uniqueness, membership testing, and mathematical set operations like union, intersection, difference, and symmetric difference.

**Key characteristics of sets:**
*   **Unordered**: Items in a set do not have a defined order, and you cannot access them using indexes.
*   **Unique Elements**: Each element in a set must be unique; duplicates are automatically removed.
*   **Mutable**: You can add or remove elements after creation.
*   **No Indexing/Slicing**: Due to their unordered nature, sets do not support indexing or slicing.
*   **Iterable**: You can iterate over the elements of a set.
*   **Hashable Elements**: Only immutable (hashable) objects can be stored within a set. Therefore, lists or other sets cannot be elements of a set.
"""

# Creating a set
my_set = {'apple', 'banana', 'cherry', 'apple', 'date'}
print(f"My set: {my_set}")
